package com.huatian.array;

public class Demo1Array {
    public static void main(String[] args) {
        /**
         * 数组的定义格式
         * 1.数据类型[]  数组名
         * 2.数据类型  数组名【】
         */
        int[] arr = new int[5];
        System.out.println(arr);
        byte[] bytes = new byte[6];
        System.out.println(bytes);
    }
}
